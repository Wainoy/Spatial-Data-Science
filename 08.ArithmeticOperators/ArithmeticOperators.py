# Addition (+), subtraction (-), division (/ or % or //), multiplication (*) and exponent (**)
# more complex math functions can be accessed using the math module
print(10//3) #returns an integer result of the division
print(10%3) #returns the remainder of the division
print(10**3) #exponent
print(min(-1,2,3,4,5,6,7,8,9,0)) #returns -1
print(max(49,5736.5762,9987,7223))
x = round(7.7788865567788,3) #returns the number rounded off to 3 decimal places
print('x is', x)
x = round(70,100)
print('x is', x)
print(pow(2,3)) #returns 8

#Using the math module
import math
print(math.sqrt(10))
print(math.ceil(3.9999)) #rounds number up to the nearest integer [4]
print(math.floor(3.999999)) #rounds number down to the nearest integer [3]
print(math.pi) #returns the value of pi


